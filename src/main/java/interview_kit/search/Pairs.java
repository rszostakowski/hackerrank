
package interview_kit.search;

public class Pairs {

    static int pairs(int k, int[] arr) {
	System.out.println("k: " + k + " arr: " + arr);

	return 0;
    }

    public static void main(String[] args) {
	int k  =  2;
	int[] arr = new int[] {1, 5, 3, 4, 2};
        int res = pairs(k, arr);
	System.out.println("result: " + res);
    }
}

